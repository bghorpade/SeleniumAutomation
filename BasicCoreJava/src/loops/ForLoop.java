package loops;

public class ForLoop 
{
	public static void main(String[] args) 
	{
		// Initilazation //condition //Increment or decrement
		for(int i = 1; i <=5; i++)
		{
			System.out.println(i);
		}
		
System.out.println();

		for(int i =5; i >=1; i--)
		{
			
			System.out.println(i);
		}
	}

}
