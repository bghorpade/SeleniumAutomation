package variabeAndDataTypes;

public class ArithematcOperators 

{
	public static void main(String[] args) 
	{
		int a = 10;
		int b = 20;	
		
		//Addition
		int sum = a+b;
		System.out.println(sum);
		
		//Subtraction
		int sub = b-a;
		System.out.println(sub);
		
		//Division
		int div= b/a;
		System.out.println(div);
		
		//Multiplication
		int mul=a*b;
		System.out.println(mul);
		
		//Modulus
		int mod = a%b;
		System.out.println(mod);
			
		
	}
}
